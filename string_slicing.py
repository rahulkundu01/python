###### To print the value through Index 
lang = "Python"
print(lang[0])

#####  To print the multiple values in index
lang1 = "Python"
#syntax - [start argument : stop argument]
print(lang1[0:3])

#####  Step Argument
#syntax - [start argument : stop argument : step]
print("Rahul"[0:4:2])
print("Rahul"[0::2])
print("Rahul"[::2])
print("Rahul"[0:5:1])
print("Rahul"[0:5:3])
print("Rahul"[5:0:-1])